package com.engine.utilities;

public class Colors {
    public static final String RESET = "\u001B[0m";  // Reset to default color
    public static final String RED = "\u001B[91m";
    public static final String GREEN = "\u001B[92m";
    public static final String YELLOW = "\u001B[93m";
    public static final String BLUE = "\u001B[94m";
    public static final String PURPLE = "\u001B[95m";
    public static final String CYAN = "\u001B[96m";
    public static final String PINK = "\u001B[38;5;200m";    // Pink
    public static final String BROWN = "\u001B[38;5;94m";    // Brown
}
