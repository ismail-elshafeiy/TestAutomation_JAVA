package java.coursesJava;

public class TestRelational {

	public static void main(String[] args) {
		int x = 25, y = 50;

		// == Equal To Operator
		System.out.println("Is 25 equal to 50 ? " + (x == y));

		// != Not Equal To Operator
		System.out.println("Is 25 not equal to 50 ? " + (x != y));

		// > Greater Than Operator
		System.out.println("Is 25 greater than 50 ? " + (x > y));

		// >= Greater Than or equal Operator
		System.out.println("Is 25 greater than or equal 50 ? " + (x >= y));

		// < Less Than Operator
		System.out.println("Is 25 Less than 50 ? " + (x < y));

		// <= Less Than or equal Operator
		System.out.println("Is 25 less than or equal 50 ? " + (x <= y));

	}

}
